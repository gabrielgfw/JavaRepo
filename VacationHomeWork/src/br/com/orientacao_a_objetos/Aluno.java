package br.com.orientacao_a_objetos;

public class Aluno {

	private String nomeAluno;
	private int rgAluno;
	private String dtNascimentoAluno;
	private Turma turmaAluno;
	
	
	public String getNomeAluno() {
		return nomeAluno;
	}
	public void setNomeAluno(String nomeAluno) {
		this.nomeAluno = nomeAluno;
	}
	public int getRgAluno() {
		return rgAluno;
	}
	public void setRgAluno(int rgAluno) {
		this.rgAluno = rgAluno;
	}
	public String getDtNascimentoAluno() {
		return dtNascimentoAluno;
	}
	public void setDtNascimentoAluno(String dtNascimentoAluno) {
		this.dtNascimentoAluno = dtNascimentoAluno;
	}
	public Turma getTurmaAluno() {
		return turmaAluno;
	}
	public void setTurmaAluno(Turma turmaAluno) {
		this.turmaAluno = turmaAluno;
	}
	
	
	
	
	
}
